#include <stdio.h>
#include <stdlib.h>
/**
 * add - add two int
 * @a: first int
 * @b: second int
 * return: addition
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - sub two int
 * @a: first int
 * @b: second int
 * return: subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mod - add two int
 * @a: first int
 * @b: second int
 * return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 * div - div two int
 * @a: first int
 * @b: second int
 * return: division
 */
#include <stdlib.h>
int div(int a, int b)
{
	return (a / b);
}
/**
 * mul - mul two int
 * @a: first int
 * @b: second int
 * return: Multiply
 */
int mul(int a, int b)
{
	return (a * b);
}
